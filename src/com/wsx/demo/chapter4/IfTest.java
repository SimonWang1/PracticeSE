package com.wsx.demo.chapter4;

public class IfTest {
	// If判断
	public static void main(String[] args) {
		int age = 24;
		if (age > 20) {
			System.out.println("年龄已经超过20岁了");
			System.out.println("到了应该承担责任的年纪...");
		}
		int a = 4;
		if (a > 5)
			System.out.println("输入的数字大于4");
		else
			System.out.println("输入的数字小于5");
	}
}
